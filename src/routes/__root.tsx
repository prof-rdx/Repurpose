function RootShell({ children }: { children: ReactNode }) {
  return (
    <html lang="en" className="dark">
      <head>
        <HeadContent />
      </head>
      <body className="bg-background text-foreground">
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();
  const router = useRouter();
  const previousUserId = useRef<string | null>(null);
  useEffect(() => {
    const { data: sub } = supabase.auth.onAuthStateChange((event, session) => {
      // Skip TOKEN_REFRESHED and INITIAL_SESSION — they fire on tab focus/mount
      // and would thrash router + cache for no identity change.
      if (event !== "SIGNED_IN" && event !== "SIGNED_OUT" && event !== "USER_UPDATED") return;
      const nextId = session?.user?.id ?? null;
      if (event === "SIGNED_IN" && nextId === previousUserId.current) return;
      previousUserId.current = nextId;
      if (event === "SIGNED_OUT") {
        queryClient.clear();
      }
      // router.invalidate() re-runs loaders, which in turn refetches active
      // queries — no need to invalidate queries by key as well.
      router.invalidate();
    });
    return () => sub.subscription.unsubscribe();
  }, [router, queryClient]);

  return (
    <QueryClientProvider client={queryClient}>
      <Outlet />
      <Toaster richColors position="top-center" />
    </QueryClientProvider>
  );
}
